public class Foo {
  public String foo() {
    return "foo";
  }

  public String foo(String a) {
    return "foo" + a;
  }

  public String foo(String a, String b) {
    return "foo" + a + b;
  }

  public String foo(String a, String b, String c) {
    return "foo" + a + b + c;
  }

  public String foo(String a, String b, String c, String d) {
    return "foo" + a + b + c + d;
  }
}
